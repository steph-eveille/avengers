package com.bnpp.bcefit.lafabrique.saison7.avengers;

public class Humanoid {
    private String name;
    private int age;

    public Humanoid(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}


